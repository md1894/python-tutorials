# membership operator 
# in , not in
# used to check weather an object is present in the collection or not
# collection -> string , list, set, tuple, dict

l = ['mehul', 'nakul', 'narendra', 'meena']
print('mehul' in l) # true
print('papa' in l) #false

print('mehul' not in l) # false
print('papa' not in l) # true